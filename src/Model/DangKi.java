package Model;

public class DangKi {
	private String maHV;
	private String maLH;
	
	public DangKi(String maHV, String maLH) {
		super();
		this.maHV = maHV;
		this.maLH = maLH;
	}
	public String getMaHV() {
		return maHV;
	}
	public void setMaHV(String maHV) {
		this.maHV = maHV;
	}
	public String getMaLH() {
		return maLH;
	}
	public void setMaLH(String maLH) {
		this.maLH = maLH;
	}
	
}
